package org.incenp.obofoundry.idrange;

/**
 * Represents an ID range policy, assigning ID ranges to users.
 */
public interface IIDRangePolicy {

    /**
     * Gets the prefix of IDs generated by this policy.
     * 
     * @return The prefix associated with this policy.
     */
    public String getPrefix();

    /**
     * Gets the short name of the policy's prefix.
     * 
     * @return The prefix name.
     */
    public String getPrefixName();

    /**
     * Gets the number of digits (padded with leading zeros) in the numerical part
     * of IDs generated with this policy.
     * 
     * @return The number of digits for this policy.
     */
    public int getWidth();

    /**
     * Gets the ID range allocated to the specified user.
     * 
     * @param name The name of the user for which to retrieve the ID range.
     * @return The range allocated to the user, or {@code null} if the policy has no
     *         range for that user.
     */
    public IDRange getRange(String name);
}
